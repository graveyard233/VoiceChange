package com.example.voicechange.Info;

public class ContentModule {


        private String color ;
        private String focus_color ;
        private String focus_bg_color ;
        private String size ;
        private String show ;
        public void setColor (String color ) {
            this.color  = color ;
        }
        public String getColor () {
            return color ;
        }

        public void setFocus_color (String focus_color ) {
            this.focus_color  = focus_color ;
        }
        public String getFocus_color () {
            return focus_color ;
        }

        public void setFocus_bg_color (String focus_bg_color ) {
            this.focus_bg_color  = focus_bg_color ;
        }
        public String getFocus_bg_color () {
            return focus_bg_color ;
        }

        public void setSize (String size ) {
            this.size  = size ;
        }
        public String getSize () {
            return size ;
        }

        public void setShow (String show ) {
            this.show  = show ;
        }
        public String getShow () {
            return show ;
        }
}
